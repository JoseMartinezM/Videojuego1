def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for _ in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def generate_map():
    """Generate a new map with some modifications."""
    num_changes = randint(8, 10)
    indices_to_change = [randint(0, len(tiles) - 1) for _ in range(num_changes)]
    for i in indices_to_change:
        tiles[i] = 1 - tiles[i]  # Toggle between 0 and 1

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

        # Slightly increase Pacman's speed
        aim.x *= 1.01
        aim.y *= 1.01

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    move_ghosts()

    for point, course in ghosts:
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 15:
            print("Game over!")
            return

    ontimer(move, 100)

def move_ghosts():
    "Move ghosts randomly."
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            course.x = choice([-5, 5])
            course.y = choice([-5, 5])

        # Make ghosts chase Pacman
        for ghost in ghosts:
            point, course = ghost
            if abs(pacman.x - point.x) > abs(pacman.y - point.y):
                if pacman.x > point.x:
                    course.x = 5
                else:
                    course.x = -5
                course.y = 0
            else:
                if pacman.y > point.y:
                    course.y = 5
                else:
                    course.y = -5
                course.x = 0

def start_game():
    "Start a new game."
    generate_map()
    setup(420, 420, 370, 0)
    bgcolor('black')
    tracer(False)
    world()
    writer.color('white')
    writer.penup()
    writer.goto(-180, 160)
    writer.write(state['score'])
    listen()
    onkey(lambda: aim.set(-5, 0), 'Left')
    onkey(lambda: aim.set(5, 0), 'Right')
    onkey(lambda: aim.set(0, 5), 'Up')
    onkey(lambda: aim.set(0, -5), 'Down')
    move()
    done()

if __name__ == '__main__':
    start_game()
