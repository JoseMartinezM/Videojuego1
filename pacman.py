def generate_map():
    global tiles
    tiles = original_tiles[:]
    indices = [i for i, tile in enumerate(tiles) if tile == 0]
    num_zeros = len(indices)
    num_changes = min(max(num_zeros // 10, 1), 10)
    indices_to_change = sample(indices, num_changes)
    for i in indices_to_change:
        tiles[i] = 1 if tiles[i] == 0 else 0

def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)
    if tiles[index] == 0:
        return False

    index = offset(point + 19)
    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    move_ghosts()

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            course.x *= -1
            course.y *= -1
            point.move(course)

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    if abs(pacman - point) < 15:
        writer.undo()
        writer.write(f"Game Over! Final Score: {state['score']}")
        return

    if not valid(pacman + aim):
        aim.x = -aim.x
        aim.y = -aim.y

    ontimer(move, 100)

def move_ghosts():
    "Move all ghosts."
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            course.x *= -1
            course.y *= -1
            point.move(course)

def change_aim(x, y):
    "Change pacman direction."
    if x == 0 and y == 0:
        return

    if abs(x) > abs(y):
        if valid(pacman + vector(x, 0)):
            aim.x = x
            aim.y = 0
    else:
        if valid(pacman + vector(0, y)):
            aim.x = 0
            aim.y = y

def start_game():
    "Initialize the game."
    generate_map()
    writer.clear()
    world()
    move()

setup(420, 420, 0, 0)
