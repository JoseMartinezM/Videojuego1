def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def modify_map(tiles):
    """Modify a portion of the map."""
    new_tiles = tiles[:]
    num_changes = int(len(tiles) * 0.07)  
    indices = list(range(len(tiles)))
    change_indices = choice(indices, num_changes, replace=False)
    
    for index in change_indices:
        new_tiles[index] = 1 if tiles[index] == 0 else 0  
    
    return new_tiles

def new_game():
    """Start a new game with a modified map."""
    global tiles, pacman, aim, ghosts
    tiles = modify_map(tiles)  
    pacman = vector(-40, -80)
    aim = vector(5, 0)
    state['score'] = 0
    for ghost in ghosts:
        ghost[0] = vector(randint(-180, 180), randint(-160, 160))
        ghost[1] = vector(choice([5, -5]), choice([5, -5]))
    writer.undo()
    writer.write(state['score'])
    clear()
    world()
    move()

def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        dx = pacman.x - point.x
        dy = pacman.y - point.y
        options = [
            vector(5, 0) if dx > 0 else vector(-5, 0),
            vector(0, 5) if dy > 0 else vector(0, -5),
        ]
        if abs(dx) > abs(dy):
            primary, secondary = options
        else:
            secondary, primary = options
        if valid(point + primary):
            point.move(primary)
        elif valid(point + secondary):
            point.move(secondary)
        else:
            plan = choice(options + [course])
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 50)

def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

