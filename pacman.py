def move_ghosts():
    "Move ghosts in the game."
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            if randint(0, 1) == 0:
                course.x = -course.x
            else:
                course.y = -course.y

def change_map():
    "Change the map at the start of the game."
    generate_map()
    world()

