tiles = list(base_tiles)  # Copia de la base del tablero

def square(x, y):
    """Dibuja un cuadrado en las coordenadas (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    """Devuelve el offset de un punto en el tablero."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    """Verifica si un punto es válido dentro del tablero."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def randomize_tiles():
    """Modifica ligeramente el tablero de juego."""
    global tiles
    tiles = list(base_tiles)
    for i in range(len(tiles)):
        if tiles[i] == 1 and randint(1, 10) == 1:
            tiles[i] = 0
        elif tiles[i] == 0 and randint(1, 20) == 1:
            tiles[i] = 1

def world():
    """Dibuja el mundo usando el tablero."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    """Mueve a Pacman y a los fantasmas en el tablero."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        options = [
            vector(8.5, 0),
            vector(-8.5, 0),
            vector(0, 8.5),
            vector(0, -8.5),
        ]
        
        plan = min(options, key=lambda v: abs(pacman - (point + v)))
        if valid(point + plan):
            point.move(plan)
        else:
            valid_options = [v for v in options if valid(point + v)]
            if valid_options:
                plan = min(valid_options, key=lambda v: abs(pacman - (point + v)))
                point.move(plan)
        
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 80)  # Controla la velocidad de los fantasmas

def change(x, y):
    """Cambia la dirección de Pacman si es válida."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

def reset_game():
    """Reinicia el estado del juego."""
    global pacman, ghosts, state
    randomize_tiles()
    pacman = vector(-40, -80)
    ghosts = [
        [vector(-180, 160), vector(5, 0)],
        [vector(-180, -160), vector(0, 5)],
        [vector(100, 160), vector(0, -5)],
        [vector(100, -160), vector(-5, 0)],
    ]
    state['score'] = 0
    path.clear()
    writer.clear()
    world()
    move()

